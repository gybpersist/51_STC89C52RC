/**
 * @brief 发送多个数据字节的函数 [数据字节中分为 : 显示数据和控制数据]
 *
 * @param bytes 要发送的多个字节
 * @param len 数组的长度 [数据字节的个数]
 * @param is_data 1 : 发送显示数据 0 : 发送控制数据
 */
static void Int_OLED_SendDataByte(u8 *bytes, u8 len, bit is_data)
{
    u8 i;
    // 主设备发送起始信号
    Dri_IIC_Start();
    // 主设备发送从设备地址和读写标识
    Dri_IIC_SendByte(OLED_ADDR);
    // 接收确认信号
    Dri_IIC_ReceiveAck();

    // 主设备发控制命令给OLED
    // 后续发的都是数据字节,没有控制命令了 --> Co -> 0
    // 发显示数据 : 01000000 ,发控制数据 : 00000000
    Dri_IIC_SendByte(is_data ? 0x40 : 0x00);
    // 接收确认信号
    Dri_IIC_ReceiveAck();

    // 循环遍历数组,把数组中的显示数据和控制数据发给OLED
    for (i = 0; i < len; i++)
    {
        // 发送显示数据or控制数据
        Dri_IIC_SendByte(bytes[i]);
        // 接收确认信号
        Dri_IIC_ReceiveAck();
    }

    // 发送停止信号
    Dri_IIC_Stop();
}

// 发控制数据的函数
static void Int_OLED_WriteCommand(u8 *cmds, u8 len)
{
    Int_OLED_SendDataByte(cmds, len, 0);
}

// 发显示数据的函数
static void Int_OLED_WriteData(u8 *datas, u8 len)
{
    Int_OLED_SendDataByte(datas, len, 1);
}

/**
 * @brief 设置字模的页地址和列地址,为显示字模做准备
 *
 * @param page_addr 页地址
 * @param column_address 列地址
 */
static void Int_OLED_SetAddress(u8 page_address, u8 column_address)
{
    // 已知 : 显示字模用 页寻址 [和清屏的寻址方式不一样]

    u8 commands[1 + 2]; // 1 : 页地址需要的命令个数 , 2 : 列地址需要的命令个数
    // 设置页地址
    commands[0] = 0xB0 + page_address;
    // 设置列地址
    // 设置列的低四位地址 : 先把命令的默认值改为 00001111 [0F] , 再 & 上 column_address
    commands[1] = 0x0F & column_address;
    // 设置列的高三位地址 : 先把命令的默认值改为 00010000 [10] , 再column_address右位移4位 , 最后 + 上column_address右位移4位的值
    commands[2] = 0x10 + (column_address >> 4);

    //把控制数据发给OLED
    Int_OLED_WriteCommand(commands,3);
}

